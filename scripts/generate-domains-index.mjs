import { promises as fs } from 'fs';
import path from 'path';

const DOMAINS_DIR = path.resolve('src/server/domains');
const OUT_FILE = path.resolve('src/server/registry/generated-domains.ts');

async function main() {
  const entries = await fs.readdir(DOMAINS_DIR, { withFileTypes: true });
  const domains = [];

  for (const entry of entries) {
    if (entry.isDirectory()) {
      const manifestPathTs = path.join(DOMAINS_DIR, entry.name, 'manifest.ts');
      const manifestPathJs = path.join(DOMAINS_DIR, entry.name, 'manifest.js');
      const found =
        (await fs
          .stat(manifestPathTs)
          .then((s) => s.isFile())
          .catch(() => false)) ||
        (await fs
          .stat(manifestPathJs)
          .then((s) => s.isFile())
          .catch(() => false));
      if (found) {
        domains.push(entry.name);
      }
    }
  }

  const imports = domains
    .map((domain, i) => `import manifest_${i} from '../domains/${domain}/manifest.js';`)
    .join('\n');
  const array = `\nexport const generatedManifests = [\n${domains.map((_, i) => `  manifest_${i}`).join(',\n')}\n];`;
  const content = `// AUTO-GENERATED BY scripts/generate-domains-index.mjs\n// DO NOT EDIT DIRECTLY\n\n${imports}\n${array}\n`;

  await fs.writeFile(OUT_FILE, content, 'utf-8');
  console.log(`[generate-domains-index] Generated registry with ${domains.length} domains.`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
